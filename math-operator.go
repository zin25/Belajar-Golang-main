package main

import "fmt"

func main() {

	var hasil = 10 + 10
	fmt.Println(hasil)

	var nilai1 = 10
	var nilai2 = 10
	var kali = nilai1 * nilai2
	fmt.Println(kali)

	var i = 10
	i += 10 // i = i + 10
	fmt.Println(i)

	i++ // i = i + 1
	fmt.Println(i)

	var negative = -100
	var positive = 100 // ga perlu +100 karna nilainya uda positive
	fmt.Println(negative)
	fmt.Println(positive)

}
