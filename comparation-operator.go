package main

import "fmt"

func main() {

	var nama = "Azzin"
	var nama2 = "Asep"
	fmt.Println(nama == nama2) //nama1 apakah sama dengan nama 2?

	var nilai1 = 100
	var nilai2 = 200

	fmt.Println(nilai1 > nilai2)  // lebih
	fmt.Println(nilai1 < nilai2)  // kurang dari
	fmt.Println(nilai1 >= nilai2) //lebih dari sama dengan
	fmt.Println(nilai1 != nilai2) //tidak sama dengan

}
